// Package ghproxy implements a reverse proxy for the GitHub API that enforces
// repository-level scoping on the user's GitHub token. The proxy runs inside
// the dcx process, listens on HTTPS with a self-signed certificate, and
// forwards allowed requests to api.github.com after rewriting the Host header
// and injecting the host's GitHub token. Requests targeting repositories other
// than the configured one are rejected with 403 Forbidden.
package ghproxy

import (
	"context"
	"crypto/ecdsa"
	"crypto/elliptic"
	"crypto/rand"
	"crypto/tls"
	"crypto/x509"
	"crypto/x509/pkix"
	"encoding/pem"
	"fmt"
	"io"
	"log/slog"
	"math/big"
	"net"
	"net/http"
	"net/http/httputil"
	"net/url"
	"os"
	"strings"
	"sync"
	"time"

	"github.com/kobus-v-schoor/dcx/internal/config"
)

// ProxyHost is the hostname used in the TLS certificate's Subject Alternative
// Name. The proxy generates a self-signed certificate with this hostname so
// the gh CLI can verify the TLS connection. In practice, GH_HOST is set to
// the Docker gateway IP (detected at runtime) plus the proxy port, so the
// certificate also includes an IP SAN for the gateway. The hostname SAN
// provides a fallback for environments where host.docker.internal is
// routable.
const ProxyHost = "host.docker.internal"

// DefaultCACertPath is the default container path where the CA certificate is
// copied so the gh CLI trusts the proxy's self-signed TLS certificate. Can be
// overridden via Options.CACertPath. Defined as a constant here for
// convenience; the canonical source is config.DefaultGHCLICACertPath.
const DefaultCACertPath = config.DefaultGHCLICACertPath

// DefaultAPIURL is the default GitHub API URL that the proxy forwards requests
// to. Can be overridden via Options.APIURL for GitHub Enterprise Server.
// Defined as a constant here for convenience; the canonical source is
// config.DefaultGHCLIAPIURL.
const DefaultAPIURL = config.DefaultGHCLIAPIURL

// DefaultCertExpiry is the default validity duration for the self-signed TLS
// certificates generated by the proxy. The certificates are ephemeral — they
// only need to last for the duration of a dcx exec session. Can be overridden
// via Options.CertExpiry. Defined as a constant here for convenience; the
// canonical source is config.DefaultGHCLICertExpiry.
const DefaultCertExpiry = config.DefaultGHCLICertExpiry

// Options holds the configuration for creating and starting a GitHub API
// proxy. All fields have sensible defaults — zero values fall back to those
// defaults. Construct via DefaultOptions for a starting point with defaults
// pre-filled.
type Options struct {
	// Token is the user's GitHub token read from the host (via gh auth token
	// or the GITHUB_TOKEN/GH_TOKEN env var). It is never written to disk or
	// logged. The proxy injects it as the Authorization header on forwarded
	// requests, replacing whatever the container-side gh CLI sends.
	Token string

	// Repository is the allowed repository in "owner/repo" format. Requests
	// targeting a different repository are rejected with 403 Forbidden. If
	// empty, all repository-scoped requests are allowed (no scoping enforced).
	Repository string

	// GatewayIP is the host's IP address on the Docker bridge network. The
	// container uses this IP to reach the proxy. It is included in the TLS
	// certificate's IP SANs so the gh CLI can verify the connection.
	GatewayIP string

	// BindAddr is the address the proxy listens on. Defaults to GatewayIP
	// (more secure — only reachable from the container's network) if empty.
	// Set to "0.0.0.0" to listen on all interfaces (needed in some Docker
	// network setups).
	BindAddr string

	// APIURL is the GitHub API URL to forward requests to. Defaults to
	// "https://api.github.com" if empty. Override for GitHub Enterprise
	// Server (e.g. "https://github.example.com/api/v3").
	APIURL string

	// CACertPath is the container path where the CA certificate is copied.
	// Defaults to "/opt/dcx/gh-proxy/ca.crt" if empty. The CA cert is
	// referenced by both NODE_EXTRA_CA_CERTS (for Node.js-based tools) and
	// the combined CA bundle referenced by SSL_CERT_FILE (for Go-based
	// programs).
	CACertPath string

	// CertExpiry is the duration for which the generated TLS certificates
	// (both CA and server) are valid. Defaults to 24 hours if zero. The
	// certificates are ephemeral — they only need to last for the duration
	// of a dcx exec session.
	CertExpiry time.Duration

	// AllowedPaths is a list of non-repository API path prefixes that are
	// allowed through the proxy even when they don't match the configured
	// repository. If empty, all non-repo paths are allowed (default behaviour).
	// When non-empty, only paths that start with one of the listed prefixes
	// are allowed through in addition to repo-scoped paths. For example,
	// ["/user", "/graphql"] allows /user and /graphql but blocks /orgs/...
	AllowedPaths []string
}

// caCertPath returns the effective CA cert container path, using the default
// if the option is empty.
func (o Options) caCertPath() string {
	if o.CACertPath != "" {
		return o.CACertPath
	}
	return DefaultCACertPath
}

// caBundlePath returns the container path for the combined CA bundle (system
// certs + proxy CA cert). This is derived from the CA cert path by replacing
// the extension with "-bundle.crt". The combined bundle is used for
// SSL_CERT_FILE so that Go programs trust both the system CAs and the proxy's
// self-signed CA. The proxy CA cert alone is used for NODE_EXTRA_CA_CERTS,
// which appends to the system trust store rather than replacing it.
func (o Options) caBundlePath() string {
	base := o.caCertPath()
	// Replace .crt extension with -bundle.crt
	if strings.HasSuffix(base, ".crt") {
		return base[:len(base)-4] + "-bundle.crt"
	}
	return base + "-bundle"
}

// apiURL returns the effective GitHub API URL, using the default if the
// option is empty.
func (o Options) apiURL() string {
	if o.APIURL != "" {
		return o.APIURL
	}
	return DefaultAPIURL
}

// certExpiry returns the effective certificate expiry duration, using the
// default if the option is zero.
func (o Options) certExpiry() time.Duration {
	if o.CertExpiry > 0 {
		return o.CertExpiry
	}
	return DefaultCertExpiry
}

// bindAddr returns the effective bind address, using GatewayIP if the option
// is empty. Binding to the gateway IP only (rather than 0.0.0.0) is more
// secure as it limits the proxy's attack surface to the Docker bridge network.
func (o Options) bindAddr() string {
	if o.BindAddr != "" {
		return o.BindAddr
	}
	return o.GatewayIP
}

// CACertPathResolved returns the effective CA cert container path, using the
// default if the option is empty.
func (o Options) CACertPathResolved() string {
	return o.caCertPath()
}

// CABundlePathResolved returns the container path for the combined CA bundle
