// SSL_CERT_FILE so that Go programs trust both the system CAs and the proxy's
// self-signed CA.
func (o Options) CABundlePathResolved() string {
	return o.caBundlePath()
}

// APIURLResolved returns the effective GitHub API URL, using the default if
// the option is empty.
func (o Options) APIURLResolved() string {
	return o.apiURL()
}

// CertExpiryResolved returns the effective certificate expiry duration, using
